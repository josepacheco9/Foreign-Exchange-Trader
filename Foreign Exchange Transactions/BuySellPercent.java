// Sets and gets the buying and selling percentage.
package ittc.machineProblem;

public class BuySellPercent {
	
	public double buyPercent;
	public double sellPercent;
	
	public void setBuyPercent(double buyPercent) {
		this.buyPercent = buyPercent;
	}
	public double getBuyPercent() {
		return buyPercent; 
	}
	public void setSellPercent(double sellPercent) {
		this.sellPercent = sellPercent;
	}
	public double getSellPercent() {
		return sellPercent;
	}
}
